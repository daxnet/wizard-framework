namespace WizardFramework
{
    /// <summary>
    /// Represents the display behavior of a wizard page. 
    /// </summary>
    public enum WizardPageDisplay
    {
        /// <summary>
        /// Indicates that the wizard page should be shown on the wizard. 
        /// </summary>
        Show,

        /// <summary>
        /// Indicates that the wizard page should be hidden from the wizard. 
        /// </summary>
        Hide
    }
}