namespace WizardFramework
{
    /// <summary>
    /// Represents the types of a wizard page. 
    /// </summary>
    public enum WizardPageType
    {
        /// <summary>
        /// Indicates that the title area will be shown on top of the wizard form when opening the
        /// current wizard page.
        /// </summary>
        Standard,

        /// <summary>
        /// Indicates that the title area will be hidden from the wizard form when opening the
        /// current wizard page.
        /// </summary>
        Expanded
    }
}